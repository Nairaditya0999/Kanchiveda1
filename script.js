function moveSlide(id, direction) {
    if (sliderStates[id] === undefined) sliderStates[id] = 0;
    let currentIdx = sliderStates[id] + direction;
    
    if (currentIdx > 2) currentIdx = 0;
    if (currentIdx < 0) currentIdx = 2;
    
    setSlide(id, currentIdx);
}

function setSlide(id, index) {
    sliderStates[id] = index;
    const sliderElem = document.getElementById(`slider-${id}`);
    if (!sliderElem) return;
    
    const track = sliderElem.querySelector('.slider-track');
    const dots = sliderElem.querySelectorAll('.dot');
    
    if (track) track.style.transform = `translateX(${index * -33.3333}%)`;
    
    dots.forEach((dot, idx) => {
        if (idx === index) dot.classList.add('active');
        else dot.classList.remove('active');
    });
}

function filterCategory(category) {
    const cards = document.querySelectorAll('.product-card');
    cards.forEach(card => {
        const cardCategory = card.getAttribute('data-category');
        card.style.display = (category === 'all' || cardCategory === category) ? 'flex' : 'none';
    });

    document.querySelectorAll('.filter-btn').forEach(btn => btn.classList.remove('active'));
    if (category === 'all' && document.getElementById('btn-all')) document.getElementById('btn-all').classList.add('active');
    if (category === 'pure-silk' && document.getElementById('btn-pure')) document.getElementById('btn-pure').classList.add('active');
    if (category === 'semi-silk' && document.getElementById('btn-semi')) document.getElementById('btn-semi').classList.add('active');
}

function getAbsoluteImageUrl(relativePath) {
    if (!relativePath) return '';
    if (relativePath.startsWith('http://') || relativePath.startsWith('https://')) {
        return relativePath;
    }
    const base = window.location.href.replace(/[^/]*$/, '');
    return base + relativePath.replace(/\\/g, '/');
}

async function enquireProduct(sareeId) {
    const saree = sareeDatabase.find(s => s.id === sareeId);
    if (!saree) return;

    const phoneNumber = '919004714311';
    const imagePath = saree.images && saree.images[0] ? saree.images[0] : '';
    const currentSlide = sliderStates[sareeId] || 0;
    const activeImage = saree.images[currentSlide] || imagePath;

    const message = [
        '✨ *NEW PRODUCT ENQUIRY | KANCHIVEDA*',
        '──────────────────────────',
        `*Product Name:* ${saree.title}`,
        `*Category:* ${saree.tagName}`,
        `*Reference ID:* ${saree.description || saree.id}`,
        `*Price Status:* ${saree.price || 'Price on Request'}`,
        '──────────────────────────',
        '',
        'Dear Kanchiveda Team,',
        'I am interested in ordering this product. Could you please check stock status, real-time availability, and processing turnaround timelines?',
        '',
        'Thank you.'
    ].join('\n');

    const evt = window.event;
    const btn = evt && evt.currentTarget ? evt.currentTarget : null;
    if (btn) {
        btn.classList.add('loading');
        btn.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Processing...';
    }

    if (navigator.share && activeImage) {
        try {
            const response = await fetch(activeImage);
            if (response.ok) {
                const blob = await response.blob();
                const ext = activeImage.split('.').pop() || 'jpg';
                const file = new File([blob], `kanchiveda-${saree.id}.${ext}`, { type: blob.type });
                const shareData = { text: message, files: [file] };
                if (navigator.canShare && navigator.canShare(shareData)) {
                    await navigator.share(shareData);
                    if (btn) resetEnquireBtn(btn);
                    return;
                }
            }
        } catch (e) {
            // Native fallback
        }
    }

    window.open(`https://wa.me/${phoneNumber}?text=${encodeURIComponent(message)}`, '_blank');
    if (btn) resetEnquireBtn(btn);
}

function resetEnquireBtn(btn) {
    btn.classList.remove('loading');
    btn.innerHTML = '<i class="fab fa-whatsapp"></i> Enquire on WhatsApp';
}

