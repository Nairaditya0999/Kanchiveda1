// New Full-Screen Multi-Image Slider system handlers
function openProductModal(sareeId, initialIdx) {
    const saree = sareeDatabase.find(s => s.id === sareeId);
    if (!saree || !saree.images || saree.images.length === 0) return;

    currentModalSareeId = sareeId;
    currentModalImageIdx = initialIdx;

    const modal = document.getElementById('image-modal');
    if (modal) {
        modal.style.display = "flex";
        document.body.style.overflow = "hidden"; 
        updateModalSlide();
    }
}

function moveModalSlide(direction) {
    const saree = sareeDatabase.find(s => s.id === currentModalSareeId);
    if (!saree || !saree.images) return;

    currentModalImageIdx += direction;

    if (currentModalImageIdx >= saree.images.length) currentModalImageIdx = 0;
    if (currentModalImageIdx < 0) currentModalImageIdx = saree.images.length - 1;

    updateModalSlide();
}

function updateModalSlide() {
    const saree = sareeDatabase.find(s => s.id === currentModalSareeId);
    if (!saree) return;

    const modalImg = document.getElementById('modal-img');
    const counterElem = document.querySelector('.modal-counter');
    
    if (modalImg) {
        modalImg.style.transform = 'scale(0.95)';
        modalImg.src = saree.images[currentModalImageIdx];
        setTimeout(() => {
            modalImg.style.transform = 'scale(1)';
        }, 50);
    }

    if (counterElem) {
        counterElem.innerText = `${currentModalImageIdx + 1} / ${saree.images.length}`;
    }
}

function closeModal() {
    const modal = document.getElementById('image-modal');
    if (modal) { 
        modal.style.display = "none";
        document.body.style.overflow = "auto"; 
        currentModalSareeId = null;
    }
}

// Upgraded standard controller window hooks capturing standard key binds cleanly
window.addEventListener('keydown', function(e) {
    const modal = document.getElementById('image-modal');
    if (modal && modal.style.display === "flex") {
        if (e.key === 'Escape') closeModal();
        if (e.key === 'ArrowLeft') moveModalSlide(-1);
        if (e.key === 'ArrowRight') moveModalSlide(1);
    }
});

